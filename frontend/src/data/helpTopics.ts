export const helpTopicMap = new Map<string, HelpTopic>(helpTopics.map((topic) => [topic.slug, topic]))

export function getHelpTopic(slug: string | null | undefined) {
  if (!slug) return null
  return helpTopicMap.get(slug) ?? null
}
